<template>
    <div>
      <b-form-group v-for="(item, index) in items" :key="index">
        <b-row>
          <b-col>
            <b-form-input v-model="item.name" :placeholder="itemFirstPlaceholder"></b-form-input>
          </b-col>
          <b-col>
            <b-form-input v-model="item.amount" :placeholder="itemSeconedPlaceholder"></b-form-input>
          </b-col>
          <b-col>
            <b-button @click="removeItem(index)">Remove</b-button>
          </b-col>
        </b-row>
      </b-form-group>
      <b-button @click="addItem">Add Item</b-button>
    </div>
</template>

<script>
export default {
  props: {
      itemFirstPlaceholder: {
      type: String,
      default: 'Name'
    },
    itemSeconedPlaceholder: {
      type: String,
      default: 'Amount'
    }
  },
  data() {
    return {
      items: [{ name: '', amount: '' }]
    };
  },
  methods: {
    addItem() {
      this.items.push({ name: '', amount: '' });
    },
    removeItem(index) {
      this.items.splice(index, 1);
    }
  }
};
</script>








































































































































































































































































































































































































































































































